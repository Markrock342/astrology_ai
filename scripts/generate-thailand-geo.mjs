/**
 * Regenerate DISTRICTS in src/data/thailand-geo.ts from the open dataset:
 * https://github.com/thailand-geography-data/thailand-geography-json
 *
 * Usage: node scripts/generate-thailand-geo.mjs
 */

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const TARGET = path.join(ROOT, "src/data/thailand-geo.ts");
const BASE =
  "https://raw.githubusercontent.com/thailand-geography-data/thailand-geography-json/master/src";

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Failed to fetch ${url}: ${res.status}`);
  return res.json();
}

function extractProvinces(source) {
  const match = source.match(
    /export const PROVINCES: string\[\] = \[([\s\S]*?)\];/,
  );
  if (!match) throw new Error("Could not parse PROVINCES from thailand-geo.ts");
  return [...match[1].matchAll(/"([^"]+)"/g)].map((m) => m[1]);
}

function extractCountries(source) {
  const match = source.match(/export const COUNTRIES = (\[[\s\S]*?\]);/);
  if (!match) throw new Error("Could not parse COUNTRIES from thailand-geo.ts");
  return match[1];
}

function formatDistricts(districts) {
  const lines = ["export const DISTRICTS: Record<string, string[]> = {"];
  for (const [province, items] of Object.entries(districts)) {
    lines.push(`  ${JSON.stringify(province)}: [`);
    for (const d of items) {
      lines.push(`    ${JSON.stringify(d)},`);
    }
    lines.push("  ],");
  }
  lines.push("};");
  return lines.join("\n");
}

const source = readFileSync(TARGET, "utf8");
const provinces = extractProvinces(source);
const countries = extractCountries(source);

const [provinceRows, districtRows] = await Promise.all([
  fetchJson(`${BASE}/provinces.json`),
  fetchJson(`${BASE}/districts.json`),
]);

const codeToProvince = new Map(
  provinceRows.map((p) => [p.provinceCode, p.provinceNameTh]),
);
const provinceToCode = new Map(
  provinceRows.map((p) => [p.provinceNameTh, p.provinceCode]),
);

const grouped = new Map();
for (const row of districtRows) {
  const province = codeToProvince.get(row.provinceCode);
  if (!province) continue;
  if (!grouped.has(province)) grouped.set(province, []);
  grouped.get(province).push(row.districtNameTh);
}

const districts = {};
const missing = [];

for (const province of provinces) {
  const code = provinceToCode.get(province);
  if (!code) {
    missing.push(`unknown province in PROVINCES: ${province}`);
    districts[province] = [];
    continue;
  }
  const list = grouped.get(province) ?? [];
  if (list.length === 0) {
    missing.push(`no districts for ${province}`);
  }
  districts[province] = list;
}

const totalDistricts = Object.values(districts).reduce((n, arr) => n + arr.length, 0);

const output = `/**
 * Canonical Thai geography dataset for birth-profile dropdowns.
 *
 * Served by \`GET /api/geo/thailand\` and re-exported from \`@/lib/th-geo\` for
 * client imports. All 77 provinces and ${totalDistricts} districts/amphoe/khet.
 *
 * DISTRICTS generated by \`node scripts/generate-thailand-geo.mjs\` from
 * thailand-geography-json (MIT) — https://github.com/thailand-geography-data/thailand-geography-json
 */

export const PROVINCES: string[] = [
${provinces.map((p) => `  ${JSON.stringify(p)},`).join("\n")}
];

${formatDistricts(districts)}

export const COUNTRIES = ${countries};
`;

writeFileSync(TARGET, output, "utf8");

console.log(`Wrote ${TARGET}`);
console.log(`Provinces: ${provinces.length}`);
console.log(`Districts: ${totalDistricts}`);
if (missing.length) {
  console.warn("Warnings:");
  for (const m of missing) console.warn(`  - ${m}`);
}
